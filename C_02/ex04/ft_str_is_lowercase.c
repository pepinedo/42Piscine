int	ft_str_is_lowercase(char *str)
{
	int	c;

	c = 0;
	while (str[c])
	{
		if ((str[c] >= 'a') && (str[c] <= 'z'))
		{
			c++;
		}
		else
			return (0);
	}
	return (1);
}
