char	*ft_strlowcase(char *str)
{
	int	c;

	c = 0;
	while (str[c])
	{
		if ((str[c] >= 65) && (str[c] <= 90))
		{
			str[c] = str[c] + 32;
		}
		c++;
	}
	return (str);
}
